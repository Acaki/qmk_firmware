static bool is_gui_set = false;

void release_gui(void) {
    bool is_gui_on = get_mods() & MOD_BIT(KC_LGUI);
    if (is_gui_set && is_gui_on) {
        unregister_mods(MOD_LGUI);
        is_gui_set = false;
    }
};

void register_gui(void) {
    bool is_gui_on = get_mods() & MOD_BIT(KC_LGUI);
    if (!is_gui_on) {
        register_mods(MOD_LGUI);
        is_gui_set = true;
    }
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case QWERTY:
            if (record->event.pressed) {
                set_single_persistent_default_layer(_QWERTY);
            }
            return false;
        case LOWER:
            if (record->event.pressed) {
                layer_on(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _ADJUST);
            } else {
                layer_off(_LOWER);
                update_tri_layer(_LOWER, _RAISE, _ADJUST);
                release_gui();
            }
            return false;
        case RAISE:
            if (record->event.pressed) {
                layer_on(_RAISE);
                update_tri_layer(_LOWER, _RAISE, _ADJUST);
            } else {
                layer_off(_RAISE);
                update_tri_layer(_LOWER, _RAISE, _ADJUST);
                release_gui();
            }
            return false;
        case ADJUST:
            if (record->event.pressed) {
                layer_on(_ADJUST);
            } else {
                layer_off(_ADJUST);
            }
            return false;
        case ARROW:
            if (record->event.pressed) {
                SEND_STRING("->");
            }
            return false;
        case DBLARR:
            if (record->event.pressed) {
                SEND_STRING("=>");
            }
            return false;
        case WIN0:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_0);
                unregister_code(KC_0);
            }
            return false;
        case WIN1:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_1);
                unregister_code(KC_1);
            }
            return false;
        case WIN2:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_2);
                unregister_code(KC_2);
            }
            return false;
        case WIN3:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_3);
                unregister_code(KC_3);
            }
            return false;
        case WIN4:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_4);
                unregister_code(KC_4);
            }
            return false;
        case WIN5:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_5);
                unregister_code(KC_5);
            }
            return false;
        case WIN6:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_6);
                unregister_code(KC_6);
            }
            return false;
        case WIN7:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_7);
                unregister_code(KC_7);
            }
            return false;
        case WIN8:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_8);
                unregister_code(KC_8);
            }
            return false;
        case WIN9:
            if (record->event.pressed) {
                register_gui();
                register_code(KC_9);
                unregister_code(KC_9);
            }
            return false;
    }
    return true;
}
